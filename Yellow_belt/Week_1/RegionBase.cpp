#include <iostream>
#include <vector>
#include <map>
#include <tuple>
#include <algorithm>

using namespace std;

enum class Lang {
    DE, FR, IT
};

struct Region {
    string std_name;
    string parent_std_name;
    map<Lang, string> names;
    int64_t population;
};

tuple<const string&, const string&, const map<Lang, string>&, const int64_t&>
    GetRank(const Region& region) {
    return tie(region.std_name, region.parent_std_name, region.names, region.population);
}

bool operator<(const Region& lhs, const Region& rhs) {
    return GetRank(lhs) < GetRank(rhs);
}

int FindMaxRepetitionCount(const vector<Region>& regions) {
    int max_repetition = regions.empty() ? 0 : 1;
    map<Region, int> region_to_repetitions;
    for (auto& current_region : regions){
        max_repetition = max(max_repetition, ++region_to_repetitions[current_region]);
    }

    return max_repetition;
}

